public class PaymentManagedListLoadAction extends PaymentManagedListActionSupport {

	private static final long serialVersionUID = 1L;

	/**
	 * 画面の初期表示。<br>
	 * <br>
	 *
	 * @param なし
	 * @return String
	 * @throws NaiException
	 */
	public String requestService() throws NaiException {

		// 開始ログ
		log.info(NaikaraStringUtil.unionProcesslog("Start"));

		// SessionPaymentManagedのクリア
        SessionDataUtil.clearSessionData(SessionPaymentManaged.class.toString());

		// 支払予定年月を取得 ｼｽﾃﾑ年月-1ヶ月
		this.payment_txt = DateUtil.getSysDateYM();

		// メッセージ情報を設定する
		if (!StringUtils.isEmpty(this.message)) {
            this.addActionMessage(this.message);
        }

		// 画面を返す
		return SUCCESS;

	}

}
